    public void setTexto(String palabra)
    {
        texto.add(palabra);
    }
    
    public int searchTexto()
    {
        int valor=0;
        Set<String> repetido = new HashSet<String>(texto);
        terminal2_final = "Las palabras definidas por el usuario fueron: \n";
        for (String index : repetido) 
        {
            valor = Collections.frequency(texto, index);
            if(valor>1)
            {
                terminal2 = terminal2 + index + " : " + valor + "\n";
                terminal2_final = terminal2_final + " -> " + index + "\n";
            }                        
        }
        return valor;
    }
    
   
    
