    private void jTextArea1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextArea1KeyPressed
        // TODO add your handling code here:}
          if(evt.getKeyCode() != KeyEvent.VK_ENTER)
          {
                String cadena = jTextArea1.getText();
                
                String[] parts = cadena.split(" ");
                for(int i=0; i<parts.length; i++)
                {
                    System.out.println ("Entrada recibida por teclado es: \"" + parts[i] +"\"");
                    
                        if(parts[i].endsWith(","))
                        {
                            String aux = parts[i].substring(0, parts[i].length()-1);
                            setTexto(aux);                            
                        }
                        else
                        {
                            this.setTexto(parts[i]);
                        }
                
                        if(searchTexto()>1)
                        {
                            jTextArea2.setText(terminal2);
                            terminal2="";
                        }
                        else
                        {
                            jTextArea2.setText(terminal2);
                            terminal2="";
                        }

                }
                texto.clear();
          }
          else
          {
              jTextArea2.setText(terminal2_final);
              jTextArea1.setEnabled(false);
              jTextArea2.setEnabled(false);
          }
            //Mandamos evento a terminal 2
            //HACER ALGO            
        
    }//GEN-LAST:event_jTextArea1KeyPressed

